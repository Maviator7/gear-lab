/** 現在の状態から表示する解説テキストを返す */
export function getExplanationText(
  gear: GearPosition,
  mode: ViewMode,
  phase: ShiftPhase,
  clutchEngaged: boolean,
): string {
  if (!clutchEngaged && phase === 'idle') {
    return EXPLANATIONS.clutchDisengaged;
  }
  if (phase !== 'idle') {
    return EXPLANATIONS.phases[phase];
  }
  return EXPLANATIONS.gears[gear][mode];
}

/** シフトフェーズのラベルを返す */
export function getPhaseLabel(phase: ShiftPhase): string {
  switch (phase) {
    case 'disengage': return 'クラッチ切断中';
    case 'synchro':   return 'シンクロナイズ中';
    case 'engage':    return 'ギア係合中';
    default:          return '';
  }
}
